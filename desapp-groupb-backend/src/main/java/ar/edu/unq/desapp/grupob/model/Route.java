package ar.edu.unq.desapp.grupob.model;

public class Route {

    private double from;
    private double to;

    public Route(double from, double to) {
        this.from = from;
        this.to = to;
    }

    public double getFrom() {
        return from;
    }

    public double getTo() {
        return to;
    }
}
